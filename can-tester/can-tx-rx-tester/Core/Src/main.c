  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    // Rx msg1
    while(!(HAL_CAN_GetRxFifoFillLevel(&hcan1, CAN_RX_FIFO0) > 0)) {} //wait for message
    res = HAL_CAN_GetRxMessage(&hcan1, CAN_RX_FIFO0, &rxHeader, rxMsg1);

    // Clear Rx FIFO
    while(HAL_CAN_GetRxFifoFillLevel(&hcan1, CAN_RX_FIFO0) > 0) {
      HAL_CAN_GetRxMessage(&hcan1, CAN_RX_FIFO0, &rxHeader, rxFIFO);
    }

    // Tx msg2
    res = HAL_CAN_AddTxMessage(&hcan1, &txHeader, txMsg2, &txMailbox);
    HAL_Delay(10);

    // Rx msg2
    while(!(HAL_CAN_GetRxFifoFillLevel(&hcan1, CAN_RX_FIFO0) > 0)) {}
    res = HAL_CAN_GetRxMessage(&hcan1, CAN_RX_FIFO0, &rxHeader, rxMsg2);

    // Check messages
    if (rxMsg1[0] != msg1Check[0]
      || rxMsg1[1] != msg1Check[1]
      || rxMsg1[2] != msg1Check[2]) {
      printf("Rx NOT working, debug periph. to test Tx \n");
    } else if (rxMsg2[0] != txMsg2[0]
            || rxMsg2[1] != txMsg2[1]
            || rxMsg2[2] != txMsg2[2]) {
      printf("Rx working, Tx NOT working");
    } else {
      printf("Rx and Tx working");
    }
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
